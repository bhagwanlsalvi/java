public class booleanpro {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println("A && B= " + (a && b)); //false
        System.out.println("A || B= " + (a || b)); //true
        System.out.println("A ! B= " + (a != b)); //true
    }
}
