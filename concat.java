public class concat {
    public static void main(String[] args) {
        String name = "Bhagwan";
        String sname = "Salvi";
        System.out.println(name + " ".concat(sname));
        System.out.println(sname.toUpperCase());
        System.out.println(sname.length());
        System.out.println(name.indexOf("a"));
    }
}
